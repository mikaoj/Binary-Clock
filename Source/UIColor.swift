import UIKit

extension UIColor {
    static var ledBlue: UIColor {
        return UIColor(red: 0, green: 170/255, blue: 1, alpha: 1)
    }

    static var darkLedBlue: UIColor {
        return UIColor(red: 0, green: 128/255, blue: 1, alpha: 1)
    }

    static var almostBlack: UIColor {
        return UIColor(red: 9/255, green: 9/255, blue: 9/255, alpha: 1)
    }

    static var inactiveLed: UIColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 0.1)
    }
}
